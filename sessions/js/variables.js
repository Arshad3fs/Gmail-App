// Variables with var

var a; // declaration
console.log('variables ', a)

a = 10
console.log('variables ', a)

a = 20
console.log('variables ', a)

var b = 100; // Variable declaration + Initialisation
console.log('variables ', b)


// Constants
const c = 10000; // Variable declaration + Initialisation of type constant
console.log('variables ', c)

// c = 0; // Assignment on a const variable is not allowed, we get error here


// Variable creations with let keyword
let d = 0;
let e;

e = 200;
d = 2;
console.log('variables', e, d)
